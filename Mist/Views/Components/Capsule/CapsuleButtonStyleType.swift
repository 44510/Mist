import SwiftUI

/// Capsule Button Style Type
enum CapsuleButtonStyleType {
    /// Standard capsule with both leading and trailing edges curved
    case standard
    /// Capsule with leading edge curved only
    case leading
    /// Capsule with trailing edge curved only
    case trailing
}
